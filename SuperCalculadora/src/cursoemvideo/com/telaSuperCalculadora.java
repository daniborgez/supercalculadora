    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        panCalc.setVisible(true);
        int numero = Integer.parseInt(spnNumero.getValue().toString());
        float resto = numero % 2; 
        lblResto.setText(Float.toString(resto)); //mostrar na tela
        double elevado = Math.pow(numero, 3); 
        lblElevado.setText(String.format("%.2f",elevado));  //mostrar na tela
        double quadrada = Math.sqrt(numero);
        lblQuadrada.setText(String.format("%.2f",quadrada)); //mostrar na tela
        double cubica =  Math.cbrt(numero);
        lblCubica.setText(String.format("%.2f",cubica));
        int absoluto = Math.abs(numero); 
        lblAbsoluto.setText(Integer.toString(absoluto));
    }//GEN-LAST:event_btnCalcularActionPerformed

